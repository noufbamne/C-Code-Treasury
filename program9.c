#include<stdio.h>
void main()
{
    float a = 246.8, b = 135.79, answer;

    answer = a + b;
    printf("%f + %f = %f\n", a, b , answer);

    answer = a - b;
    printf("%f - %f = %f\n", a, b , answer);

    answer = a * b;
    printf("%f * %f = %f\n", a, b , answer);

    answer = a / b;
    printf("%f / %f = %f\n", a, b , answer);

}