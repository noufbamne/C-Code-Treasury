// Switch case
// Without using break

#include<stdio.h>
void main()
{
    int i = 2;

    switch (i)
    {
    case 1:
        printf("This is case 1.\n");

    case 2:
        printf("This is case 2.\n");

    case 3:
        printf("This is case 3.\n");

    case 4:
        printf("This is case 4.\n");
    
    default:
        printf("I am in default");
        
    }
}