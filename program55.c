// Switch case
// By using break
#include<stdio.h>
void main()
{
    int i = 2;

    switch (i)
    {
    case 1:
        printf("This is case 1.\n");
        break;

    case 2:
        printf("This is case 2.\n");
        break;

    case 3:
        printf("This is case 3.\n");
        break;

    case 4:
        printf("This is case 4.\n");
        break;
    
    default:
        printf("I am in default");
        
    }
}