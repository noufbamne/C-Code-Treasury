// C program to illustrate the logical operators
#include <stdio.h>

int main()
{

    int a = 25, b = 5;

    // using operators and printing results
    printf("a && b : %d\n", a && b); // and
    printf("a || b : %d\n", a || b); // or
    printf("!a: %d\n", !a); // not

    return 0;
}